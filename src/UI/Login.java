package UI;

public class Login {

    private Object password;
    private Object dni;
    private Object buttonRegister;
    private Object buttonForgotPassword;
    private Object labelEnterPassword;

    public Login(){

    }

    public Object getPassword() {
        return password;
    }

    public void setPassword(Object password) {
        this.password = password;
    }

    public Object getDni() {
        return dni;
    }

    public void setDni(Object dni) {
        this.dni = dni;
    }

    public Object getButtonRegister() {
        return buttonRegister;
    }

    public void setButtonRegister(Object buttonRegister) {
        this.buttonRegister = buttonRegister;
    }

    public Object getButtonForgotPassword() {
        return buttonForgotPassword;
    }

    public void setButtonForgotPassword(Object buttonForgotPassword) {
        this.buttonForgotPassword = buttonForgotPassword;
    }

    public Object getLabelEnterPassword() {
        return labelEnterPassword;
    }

    public void setLabelEnterPassword(Object labelEnterPassword) {
        this.labelEnterPassword = labelEnterPassword;
    }
}
