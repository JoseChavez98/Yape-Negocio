package UI.owner;

public class Profile {
    private Object labelOwner;
    private Object labelQR;
    private Object labelInfo;

    public Profile() {
    }

    public Object getLabelOwner() {
        return labelOwner;
    }

    public void setLabelOwner(Object labelOwner) {
        this.labelOwner = labelOwner;
    }

    public Object getLabelQR() {
        return labelQR;
    }

    public void setLabelQR(Object labelQR) {
        this.labelQR = labelQR;
    }

    public Object getLabelInfo() {
        return labelInfo;
    }

    public void setLabelInfo(Object labelInfo) {
        this.labelInfo = labelInfo;
    }
}
